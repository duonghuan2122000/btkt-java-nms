    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String name = inName.getText();
            String type = (String) inType.getSelectedItem();
            double price = Double.parseDouble(inPrice.getText());
            checkProduct(name, type, price);
            if (indexProduct == -1) {
                home.addProduct(new Product(name, type, price));
            } else {
                product.setName(name);
                product.setType(type);
                product.setPrice(price);
                home.editProduct(product, indexProduct);
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "Gia ban khong hop le.");
        } catch (ProductException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

