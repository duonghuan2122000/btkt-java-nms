    private void checkProduct(String name, String type, double price) throws ProductException {
        if (name.isEmpty()) {
            throw new ProductException("ten khong duoc de trong.");
        }
        if (type.isEmpty()) {
            throw new ProductException("nhom khong duoc de trong.");
        }
        if (price <= 0) {
            throw new ProductException("gia ban khong hop le.");
        }
    }

    private void showData() {
        if(indexProduct != -1){
            inName.setText(product.getName());
            inType.setSelectedItem(product.getType());
            inPrice.setText(String.valueOf(product.getPrice()));
        }
    }
