    private void showData() {
        if (indexCustomer != -1) {
            inFullName.setText(customer.getFullName());
            inAdd.setText(customer.getAdd());
            inPhone.setText(customer.getPhone());
        }
    }

    private void checkCustomer(String fullName, String add, String phone) throws CustomerException {
        if (fullName.isEmpty()) {
            throw new CustomerException("Ho ten khong duoc de trong.");
        }
        if (add.isEmpty()) {
            throw new CustomerException("Dia chi khong duoc de trong.");
        }

        if (phone.isEmpty() || !phone.matches("^0([0-9]{9})$")) {
            throw new CustomerException("SDT khong hop le.");
        }
    }
