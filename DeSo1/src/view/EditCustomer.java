    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String fullName = inFullName.getText();
            String add = inAdd.getText();
            String phone = inPhone.getText();
            checkCustomer(fullName, add, phone);
            if (indexCustomer == -1) {
                home.addCustomer(new Customer(fullName, add, phone));
            } else {
                customer.setFullName(fullName);
                customer.setAdd(add);
                customer.setPhone(phone);
                home.editCustomer(customer, indexCustomer);
            }

        } catch (CustomerException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

