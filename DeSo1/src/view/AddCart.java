    @Override
    public <T> void showData(List<T> l, DefaultTableModel model) {
        model.setRowCount(0);
        l.forEach(t -> {
            if (t instanceof Product) {
                model.addRow(((Product) t).toObject());
            } else if (t instanceof Customer) {
                model.addRow(((Customer) t).toObject());
            }
        });
    }

    private void showCustomer() {
        customers = home.getCustomers();
        showData(customers, tblCustomerModel);
    }

    private void showProduct() {
        products = home.getProducts();
        showData(products, tblProductModel);
    }
