    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            int iProduct = tblProduct.getSelectedRow();
            int iCustomer = tblCustomer.getSelectedRow();
            int quantity = Integer.parseInt(inQuantity.getText());
            if (iProduct == -1 || iCustomer == -1) {
                throw new Exception("Chua chon mat hang hoac khach hang.");
            }
            if (quantity <= 0) {
                throw new NumberFormatException();
            }
            Product product = products.get(iProduct);
            Customer customer = customers.get(iCustomer);
            Cart cart = new Cart(customer, product, quantity);
            boolean ok = home.addOrUpdateCart(cart);
            if(!ok){
                JOptionPane.showMessageDialog(rootPane, "Moi nguoi chi duoc phep toi da mua 10 mat hang.");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "So luong khong hop le.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

